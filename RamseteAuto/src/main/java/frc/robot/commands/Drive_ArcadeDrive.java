package frc.robot.commands;

import edu.wpi.first.wpilibj.XboxController;
import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.Drivetrain;

public class Drive_ArcadeDrive extends CommandBase {
  Drivetrain drivetrain;
  XboxController driverController;

  public Drive_ArcadeDrive(Drivetrain drivetrain, XboxController driverController) {
    addRequirements(drivetrain);

    this.drivetrain = drivetrain;
    this.driverController = driverController;
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {}

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    drivetrain.arcadeDrive(driverController.getLeftY(), driverController.getRightX());
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {}

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
